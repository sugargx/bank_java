    private void btn_yesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_yesActionPerformed
        String id_s = TF_id.getText();
        String money_s = TF_money.getText();
        int flag_id = 1;
        int flag_money = 1;
       if(money_s.charAt(0) == '.' || money_s.charAt(money_s.length() - 1) == '.')
       {
           flag_money = 0;
       }
       else{
            for(int i = 1; i < money_s.length() - 1; i++)
            {
                if((money_s.charAt(i) < '0' || money_s.charAt(i) > '9') && money_s.charAt(i) != '.')
                {
                    flag_money = 0;
                    break;
                }
            }
       }
        for (int i = 0; i < id_s.length(); i++) {
            if (id_s.charAt(i) < '0' || id_s.charAt(i) > '9') {
                flag_id = 0;
                break;
            }
        }
        if (id_s.isEmpty()) {
            JOptionPane.showMessageDialog(null, "请输入您的ID");
        } else if (flag_id == 0) {
            JOptionPane.showMessageDialog(null, "您输入的ID格式有误");
        } else if (money_s.isEmpty()) {
            JOptionPane.showMessageDialog(null, "请输入您的存款金额");
        } else if (flag_money == 0) {
            JOptionPane.showMessageDialog(null, "您输入的存款金额有误");
        } else {

            int id = Integer.parseInt(id_s);
            double money = Double.parseDouble(money_s);
            double money_all = -1;
            try {
                if (dao.idExist(id) == 0) {
                    JOptionPane.showMessageDialog(null, "ID不存在！");
                } else {
                    money_all = dao.addMoney(id, money);
                    if (money_all != -1) {
                        Date date = new Date();
                        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                        this.setVisible(false);
                        new addmoney_backFrame(money, money_all, df.format(date)).setVisible(true);
                    }
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(addmoneyFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(addmoneyFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_btn_yesActionPerformed

